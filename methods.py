def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def sum(a, b):
    return a+b

def difference(a, b):
    return a-b

def product(a, b):
    return a*b

def quotient(a, b):
    return a/b